    private void b_LoadKriteriaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_b_LoadKriteriaActionPerformed
        String sql = "Select * From tb_penilaiankriteria";
        try {
            java.sql.Statement stat = conn.createStatement();
            ResultSet hsl = stat.executeQuery(sql);
            while(hsl.next()){
                String k1 = hsl.getString("Pedagogik");
                t_PdKriteria.setText(k1);
                String k2 = hsl.getString("Kepribadian");
                t_KpKriteria.setText(k2);
                String k3 = hsl.getString("Profesional");
                t_PrKriteria.setText(k3);
                String k4 = hsl.getString("Sosial");
                t_SoKriteria.setText(k4);

                t_PdKriteria.setEnabled(false);
                t_KpKriteria.setEnabled(false);
                t_PrKriteria.setEnabled(false);
                t_SoKriteria.setEnabled(false);
            }
        }catch (Exception e){
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_b_LoadKriteriaActionPerformed

    private void b_Simpan1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_b_Simpan1ActionPerformed
        String sql = "Insert into tb_hasilakhir values (?,?)";
            try{
                PreparedStatement stat = conn.prepareStatement(sql);
                stat.setString(1, cb_alt1.getSelectedItem().toString());
                stat.setString(2, t_hasilPaijo.getText());
                
                
                stat.executeUpdate();
                JOptionPane.showMessageDialog(null, "Data Berhasil Di Tambah");
                
                cb_alt1.requestFocus(); 
            }catch (SQLException e){
                JOptionPane.showMessageDialog(null, "Data Gagal Di Tambah");
            }
    }//GEN-LAST:event_b_Simpan1ActionPerformed

