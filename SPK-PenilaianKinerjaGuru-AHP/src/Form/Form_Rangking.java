    private void hitung(){
        //Alternatif
        float paijo1 = Float.parseFloat(t_paijo1.getText());
        float paijo2 = Float.parseFloat(t_paijo2.getText());
        float paijo3 = Float.parseFloat(t_paijo3.getText());
        float paijo4 = Float.parseFloat(t_paijo4.getText());
        
        float paiman1 = Float.parseFloat(t_paiman1.getText());
        float paiman2 = Float.parseFloat(t_paiman2.getText());
        float paiman3 = Float.parseFloat(t_paiman3.getText());
        float paiman4 = Float.parseFloat(t_paiman4.getText());
        
        float paseto1 = Float.parseFloat(t_paseto1.getText());
        float paseto2 = Float.parseFloat(t_paseto2.getText());
        float paseto3 = Float.parseFloat(t_paseto3.getText());
        float paseto4 = Float.parseFloat(t_paseto4.getText());
        
        float pasuji1 = Float.parseFloat(t_pasuji1.getText());
        float pasuji2 = Float.parseFloat(t_pasuji2.getText());
        float pasuji3 = Float.parseFloat(t_pasuji3.getText());
        float pasuji4 = Float.parseFloat(t_pasuji4.getText());
        
        //KriteriaA
        float pedagogik = Float.parseFloat(t_PdKriteria.getText());
        float kepribadian = Float.parseFloat(t_KpKriteria.getText());
        float profesional = Float.parseFloat(t_PrKriteria.getText());
        float sosial = Float.parseFloat(t_SoKriteria.getText());
        
        float hitungPaijo = (pedagogik*paijo1) + (kepribadian*paijo2) + (profesional*paijo3) + (sosial*paijo4);
            String total = String.format("%.3f", hitungPaijo);
            t_hasilPaijo.setText(total);
            
        float hitungPaiman = (pedagogik*paiman1) + (kepribadian*paiman2) + (profesional*paiman3) + (sosial*paiman4);
            String total1 = String.format("%.3f", hitungPaiman);
            t_hasilPaiman.setText(total1);
            
        float hitungPaseto = (pedagogik*paseto1) + (kepribadian*paseto2) + (profesional*paseto3) + (sosial*paseto4);
            String total2 = String.format("%.3f", hitungPaseto);
            t_hasilPaseto.setText(total2); 
        
        float hitungPasuji = (pedagogik*pasuji1) + (kepribadian*pasuji2) + (profesional*pasuji3) + (sosial*pasuji4);
            String total3 = String.format("%.3f", hitungPasuji);
            t_hasilPasuji.setText(total3);
    }
    
