    private void nilaiPaijo(){
        String sql = "SELECT Paijo FROM tb_penilaianalternatif";
        try {
            java.sql.Statement stat = conn.createStatement();
            ResultSet hsl = stat.executeQuery(sql);
            int count = 1;    while (hsl.next() && count <= 4) {
              String nilai = hsl.getString("Paijo");
                switch (count) {
                case 1:
                    t_paijo1.setText(nilai);
                    break;
                case 2:
                    t_paijo2.setText(nilai);
                    break;
                case 3:
                    t_paijo3.setText(nilai);
                    break;
                case 4:
                    t_paijo4.setText(nilai);
                    break;
                default:
                    break;
        }
        count++;
        }
        }catch (Exception e) {
        JOptionPane.showMessageDialog(null, e);
        }             
    } 
    
